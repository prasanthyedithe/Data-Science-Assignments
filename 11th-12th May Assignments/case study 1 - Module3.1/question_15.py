# 15. Give example of fsum and sum function of math library

import math

# list
print(sum([1, 2, 3, 4, 5, 6, 7, 8, 9]))
# tuple
print(sum((1, 2, 3, 4, 5, 6, 7, 8, 9)))
# set
print(sum({1, 2, 3, 4, 5, 6, 7, 8, 9}))
# range
print(sum(range(1, 10)))

# list
print(math.fsum([1, 2, 3, 4, 5, 6, 7, 8, 9]))
# tuple
print(math.fsum((1, 2, 3, 4, 5, 6, 7, 8, 9)))
# set
print(math.fsum({1, 2, 3, 4, 5, 6, 7, 8, 9}))
# range
print(math.fsum(range(1, 10)))
